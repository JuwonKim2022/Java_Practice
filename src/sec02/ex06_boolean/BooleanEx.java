package sec02.ex06_boolean;

public class BooleanEx {

	public static void main(String[] args) {
		
		boolean stop = true;
		
		if(stop) {
			System.out.println("Stop!");
		} else {
			System.out.println("Go!");
		}
		
		boolean call = false;
		
		if(call) {
			System.out.println("non Call!");
		} else {
			System.out.println("Call!");
		}

	}

}
